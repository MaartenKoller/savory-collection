/**
 * Returns a random image URL or the provided image if it exists
 */
export const getRecipeImage = (image?: string): string => {
  if (image) return image;
  
  // Get a random image from the placeholders
  const randomIndex = Math.floor(Math.random() * placeholderImages.length);
  return placeholderImages[randomIndex];
};
